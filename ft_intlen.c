#include "libftprintf.h"

int	ft_intlen(int nb)
{
	int	len;

	len = 0;
	if (nb < 0)
		nb *= -1;
	while (nb > 9)
	{
		nb /= 10;
		len++;
	}
	return (len + 1);
}
